    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            double valor = Double.parseDouble(jTextField1.getText());
            double resultado;
            
            if (jRadioButton1.isSelected()) {
                resultado = valor * 0.17;
                jLabel2.setText("Resultado: $" + Double.toString(resultado));
                
            } else if (jRadioButton2.isSelected()) {
                resultado = valor * 0.17;
                jLabel2.setText("Resultado: €" + Double.toString(resultado));
                
            } else if (jRadioButton3.isSelected()) {
                resultado = valor * 26.68;
                jLabel2.setText("Resultado: ¥" + Double.toString(resultado));
                
            }
        }
        catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "ERRO: Digite um valor válido!");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

