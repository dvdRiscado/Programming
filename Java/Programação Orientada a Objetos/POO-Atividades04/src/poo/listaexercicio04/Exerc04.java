/*
Criar um programa que receba um grau e o converta para radianos através de um 
método. Fórmula: radiano = grau * pi / 180
*/

package poo.listaexercicio04;

public class Exerc04 {
    
    public double funExerc04(float grau) {
        
        double radiano = (grau * 3.14) / 180;
        
        return radiano;
    }
}
